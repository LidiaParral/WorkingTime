    /**
     * Método loadSplash: Este método permite ir cargando hasta el 100% el
     * componente ProgressBar, en una secuencia de 280 milisegundos.
     */
    private void loadSplash() {
        Thread hilo;
        hilo = new Thread() {
            @Override
            public void run() {
                for (int i = 1; i <= 100; i++) {
                    try {
                        sleep(280);
                        pgbarSplash.setValue(i);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(SplashScreen.class.getName()).log(Level.SEVERE, null, ex);
                        JOptionPane.showMessageDialog(null, "Error interno en el sistema.", "ERROR", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }

        };
        hilo.start();
    }

